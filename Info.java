
public class User {

private String id;
private String tag;
private String name;
private String description;
private String picture;
private String emil;

public String getId() {
return this.id;
}

public String getTag() {
return this.tag;
}

public String getName() {
return this.name;
}

public String getDescription() {
        return this.description;
    }

public String getPicture() {
return this.picture;
}

public String getEmail() {
return this.emil;
}
}
